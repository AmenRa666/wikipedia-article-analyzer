
var mongoose = require('mongoose');
var Schema = mongoose.Schema;

// schema maps to a MongoDB collection and defines the shape of the documents within that collection
var articlesSchema = new Schema({
  // Length Features
  characterCount: Number,
  wordCount: Number,
  syllableCount: Number,
  sentenceCount: Number,
  // Structure Features
  sectionCount: Number,
  subsectionCount: Number,
  paragraphCount: Number,
  meanSectionSize: Number,
  meanParagraphSize: Number,
  largestSectionSize: Number,
  shortestSectionSize: Number,
  largestShortestSectionRatio: Number,
  sectionSizeStandardDeviation: Number,
  meanOfSubsectionsPerSection: Number,
  abstractSize: Number,
  abstractSizeArtcileLengthRatio: Number,
  citationCount: Number,
  citationCountPerSentence: Number,
  citationCountPerSection: Number,
  externalLinksCount: Number,
  externalLinksPerSentence: Number,
  externalLinksPerSection: Number,
  imageCount: Number,
  imagePerSentence: Number,
  imagePerSection: Number,
  // Style Features
  meanSentenceSize: Number,
  largestSentenceSize: Number,
  shortestSentenceSize: Number,
  largeSentenceRate: Number,
  shortSentenceRate: Number,
  questionCount: Number,
  questionRatio: Number,
  exclamationCount: Number,
  exclamationRatio: Number,
  toBeVerbCount: Number,
  toBeVerbRatio: Number,
  toBeVerbPerSentence: Number,
  toBeVerbRate: Number,
  // Readability Features
  automatedReadabilityIndex: Number,
  colemanLiauIndex: Number,
  fleshReadingEase: Number,
  fleschKincaidGradeLevel: Number,
  gunningFogIndex: Number,
  lasbarhetsIndex: Number,
  smogGrading: Number,
  linsearWriteFormula: Number,
  daleChallReadabilityFormula: Number,
  // Lexical Features
  differentWordCount: Number,
  differentWordsPerSentence: Number,
  differentWordsRate: Number,
  nounCount: Number,
  nounsPerSentence: Number,
  nounsRate: Number,
  differentNounCount: Number,
  differentNounsPerSentence: Number,
  differentNounsRate: Number,
  verbCount: Number,
  verbsPerSentence: Number,
  verbsRate: Number,
  differentVerbCount: Number,
  differentVerbsPerSentence: Number,
  differentVerbsRate: Number,
  pronounCount: Number,
  pronounsPerSentence: Number,
  pronounsRate: Number,
  differentPronounCount: Number,
  differentPronounsPerSentence: Number,
  differentPronounsRate: Number,
  adjectiveCount: Number,
  adjectivesPerSentence: Number,
  adjectivesRate: Number,
  differentAdjectiveCount: Number,
  differentAdjectivesPerSentence: Number,
  differentAdjectivesRate: Number,
  adverbCount: Number,
  adverbsPerSentence: Number,
  adverbsRate: Number,
  differentAdverbCount: Number,
  differentAdverbsPerSentence: Number,
  differentAdverbsRate: Number,
  coordinatingConjunctionCount: Number,
  coordinatingConjunctionsPerSentence: Number,
  coordinatingConjunctionsRate: Number,
  differentCoordinatingConjunctionCount: Number,
  differentCoordinatingConjunctionsPerSentence: Number,
  differentCoordinatingConjunctionsRate: Number,
  subordinatingPrepositionAndConjunctionCount: Number,
  subordinatingPrepositionsAndConjunctionsPerSentence: Number,
  subordinatingPrepositionsAndConjunctionsRate: Number,
  differentSubordinatingPrepositionAndConjunctionCount: Number,
  differentSubordinatingPrepositionsAndConjunctionsPerSentence: Number,
  differentSubordinatingPrepositionsAndConjunctionsRate: Number,
  syllablesPerWord: Number,
  charactersPerWord: Number,
  // Quality Class
  qualityClass: Number
});

// instances of Models are documents
exports.Article = mongoose.model('articles', articlesSchema);






